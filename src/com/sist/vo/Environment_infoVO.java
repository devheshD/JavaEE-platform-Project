package com.sist.vo;

public class Environment_infoVO {
	private int year;
	private int CO;
	private int NOx;
	private int SOx;
	private int TSP;
	private int PM10;
	private int VOC;
	private int NH3;
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getCO() {
		return CO;
	}
	public void setCO(int cO) {
		CO = cO;
	}
	public int getNOx() {
		return NOx;
	}
	public void setNOx(int nOx) {
		NOx = nOx;
	}
	public int getSOx() {
		return SOx;
	}
	public void setSOx(int sOx) {
		SOx = sOx;
	}
	public int getTSP() {
		return TSP;
	}
	public void setTSP(int tSP) {
		TSP = tSP;
	}
	public int getPM10() {
		return PM10;
	}
	public void setPM10(int pM10) {
		PM10 = pM10;
	}
	public int getVOC() {
		return VOC;
	}
	public void setVOC(int vOC) {
		VOC = vOC;
	}
	public int getNH3() {
		return NH3;
	}
	public void setNH3(int nH3) {
		NH3 = nH3;
	}
	
}
